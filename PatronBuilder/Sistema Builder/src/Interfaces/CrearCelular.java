    public void grabarCelular(Telefono tel) {
        FileWriter fw;
        PrintWriter pw;
        try {
            fw = new FileWriter(ruta_txt,true);
            BufferedWriter bw = new BufferedWriter(fw);
            pw = new PrintWriter(bw);
            pw.println(String.valueOf(tel.getCarcasa() + ", " + tel.getProcesador() + ", " + tel.getGpu() + ", " + tel.getNpu()));

            pw.close();
            JOptionPane.showMessageDialog(null, "Operaci√≥n realizada correctamente");
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Fallo al crear");
            System.out.println(ex.getMessage());
        }
    }
